public class SWITCH
{
    public static void main(String[] args)
    {
        char color ='g';
        switch(color)
        {
            case 'b':
                System.out.println("blue");
                break;
            case 'r':
                System.out.println("red");
                break;
            case 'g':
                System.out.println("Green");
                break;
            default:
                System.out.println("Invalid");
                break;
        }
    }
}
